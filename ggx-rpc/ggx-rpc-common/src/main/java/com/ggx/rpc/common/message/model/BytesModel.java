package com.ggx.rpc.common.message.model;

public class BytesModel {
	
	private byte[] bytes;

	public BytesModel() {
	}

	public BytesModel(byte[] bytes) {
		super();
		this.bytes = bytes;
	}

	public byte[] getBytes() {
		return bytes;
	}

	public void setBytes(byte[] bytes) {
		this.bytes = bytes;
	}
	
	
	

}
